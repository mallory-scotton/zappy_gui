namespace Zappy
{

///////////////////////////////////////////////////////////////////////////////
Inventory::Inventory(void)
    : food(0)
    , linemate(0)
    , deraumere(0)
    , sibur(0)
    , mendiane(0)
    , phiras(0)
    , thystame(0)
{}

///////////////////////////////////////////////////////////////////////////////
void Inventory::ParseContent(std::istringstream& content)
{
    content >> food
            >> linemate
            >> deraumere
            >> sibur
            >> mendiane
            >> phiras
            >> thystame;
}

} // !namespace Zappy
