///////////////////////////////////////////////////////////////////////////////
// Header guard
///////////////////////////////////////////////////////////////////////////////
#pragma once

///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////

#include <string>
#include <sstream>

///////////////////////////////////////////////////////////////////////////////
// Namespace Zappy
///////////////////////////////////////////////////////////////////////////////
namespace Zappy
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class Inventory
{
public:
    ///////////////////////////////////////////////////////////////////////////
    // Public members
    ///////////////////////////////////////////////////////////////////////////
    unsigned int food;           //<! Food quantity on the tile
    unsigned int linemate;       //<! Linemate quantity on the tile
    unsigned int deraumere;      //<! Deraumere quantity on the tile
    unsigned int sibur;          //<! Sibur quantity on the tile
    unsigned int mendiane;       //<! Mendiane quantity on the tile
    unsigned int phiras;         //<! Phiras quantity on the tile
    unsigned int thystame;       //<! Thystame quantity on the tile

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief Default constructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    Inventory(void);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief Parses the content string to fill the inventory
    ///
    /// \param content The content string to parse
    ///
    ///////////////////////////////////////////////////////////////////////////
    void ParseContent(std::istringstream& content);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Draws the inventory text
    ///
    ///////////////////////////////////////////////////////////////////////////
    void DrawInvText(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Draws the inventory numbers
    ///
    ///////////////////////////////////////////////////////////////////////////
    void DrawInvNumb(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Resets the inventory to zero
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Reset(void);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Adds the resources from another inventory to this one
    ///
    /// \param other The other inventory to add resources from
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Add(const Inventory& other);
};

} // !namespace Zappy
